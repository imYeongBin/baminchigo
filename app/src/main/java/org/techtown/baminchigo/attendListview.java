package org.techtown.baminchigo;

public class attendListview {

    private String attendDt;
    private String place;
    private String memCount;
    private String totalFee;

    public String getAttendDt() {
        return attendDt;
    }

    public void setAttendDt(String attendDt) {
        this.attendDt = attendDt;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getMemCount() {
        return memCount;
    }

    public void setMemCount(String memCount) {
        this.memCount = memCount;
    }

    public String getTotalFee() {
        return totalFee;
    }

    public void setTotalFee(String totalFee) {
        this.totalFee = totalFee;
    }
}
